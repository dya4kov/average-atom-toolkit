double ThreeHalf::value(const double& x) {
	double up = 0.0, down = 0.0;
	double xpow[11] = {0.0};
	double t = 0.0;

	const int m1 = 7, m2 = 10;
	const int k1 = 8, k2 = 11;

	int i;
	xpow[0] = 1.0;
	if (x < 2.0) {
		t = exp(x);
		for (i = 1; i < k1; ++i) {
			xpow[i] = xpow[i - 1]*t;
		}
		for (i = 0; i < m1; ++i) {
			up += xpow[i]*a[i];
		}
		for (i = 0; i < k1; ++i) {
			down += xpow[i]*b[i];
		}	
	}
	else {
		t = sqrt(x);
		for (i = 1; i < k2; ++i) {
			xpow[i] = xpow[i - 1]/(x*x);
		}
		for (i = 0; i < m2; ++i) {
			up += xpow[i]*c[i];			
		}
		for (i = 0; i < k2; ++i) {
			down += xpow[i]*d[i];
		}
		t = t*x*x;
	}	
	return t*up/down;
}